#include "./libft.h"

/*
** Check if c is an alphabetic character
*/

int	ft_isalpha(int c)
{
	if ('A' <= c && c <= 'z')
	{
		if ('Z' < c && c < 'a')
			return (0);
		return (1);
	}
	return (0);
}
